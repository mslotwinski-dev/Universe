    <h3>Natura sił molekularnych</h3>
    <Section>
      Siły molekularne same w sobie nie są siłami podstawowymi czy elementarnymi
      w takim sensie jak są nimi siły ciążenia. Siły molekularne są wypadkową
      niezmiernie skomplikowanych oddziaływań wszystkich elektronów i jąder
      jednej cząsteczki z elektronami i jądrami drugiej. Wszystkie proste na
      pierwszy rzut oka wzory, które otrzymujemy, opisują sumę tych
      skomplikowanych oddziaływań, więc nie można tu mówić o zjawiskach
      podstawowych.
    </Section>
    <Section>
      Ponieważ siły molekularne przyciągają na dużych odległościach i odpychają
      na małych pozwala to na istnienie ciał stałych, w których atomy trzymają
      się mocno dzięki przyciąganiu, utrzymują zaś pewne odległości między sobą
      dzięki odpychaniu. W pewnej odległości d siły się równoważą, otrzymujemy 0
      (miejsce przecięcia osi OX). Taką właśnie odległość utrzymują cząsteczki
      ciała stałego. Aby bardzo lekko zbliżyć cząsteczki do siebie możemy
      zastąpić naszą krzywą prostą dla uproszczenia. Wtedy siła jest
      proporcjonalna do wychylenia cząsteczek. Nie mówi Ci to czegoś? Tak, to
      właśnie stanowi treść prawa Hooke'a, które prawdopodobnie znasz ze szkoły
      średniej.
    </Section>
    <h3>Siły jądrowe</h3>
    <Section>
      Zakończymy ten rozdział krótką wzmianką o siłach innej natury niż
      wszystkie poprzednie, a mianowicie siłach jądrowych. Są to siły działające
      wewnątrz jąder atomowych. Chociaż się je bada, nikt nie obliczył siły
      działającej między nukleonami jądra i na razie nie znamy żadnego prawa
      opisującego te siły. Siły jądrowe to przykład oddziaływania silnego, czyli
      siły działającej między kwarkami. Jako, że jądra składają się z kwarków,
      zatem będą podlegać temu oddziaływaniu. Siły te maja skrajnie krótki
      zasięg, mniej więcej rzędu rozmiarów jądra, czyli być może
      <M s="10^{-13}" /> cm. Gdy cząstki są tak małe i tak blisko siebie, można
      stosować tylko prawa mechaniki kwantowej, a nie prawa Newtona (ale nawet
      teoria kwantów nie wystarczy nam do opisu skrajnie mocnych zderzeń w
      akceleratorach - tutaj wkracza tak zwana kwantowa teoria pola). W
      zagadnieniach fizyki jądrowej nie korzystamy już nawet z pojęcia siły,
      zastępując je mianowicie pojęciem energii oddziaływania między dwiema
      cząstkami.
    </Section>
