      Zastanówmy się teraz, co by się stało, gdybyśmy takie samo doświadczenie
      przeprowadzili w drugim pociągu (układ S"), który porusza się coraz
      szybciej do przodu. W miarę jak pociąg przyspiesza, kostka lodu pozostaje
      coraz bardziej z tyłu — w stosunku do S" kostka przyspiesza do tyłu, mimo
      że całkowita działająca na nią siła jest równa zeru. Jasno widać, że układ
      S" jest nieinercjalnym układem odniesienia i że żadne z dwóch praw
      dynamiki nie może być w nim spełnione. Do podobnego wniosku doszlibyśmy,
      gdyby układ S" był związany z wirującą karuzelą. Klocek, gdy całkowita
      siła działająca na niego jest równa zeru, nie będzie poruszał się względem
      S" ruchem prostoliniowym, zatem zasady dynamiki Newtona nie będą
      spełnione.
    </Section>
    <h3>Zakres stosowalności zasad newtona</h3>
    <Section>
      Widzimy więc, że dwie zasady dynamiki Newtona są słuszne jedynie w
      szczególnych — inercjalnych — układach odniesienia, które (mówiąc niezbyt
      precyzyjnie) nie poruszają się ruchem przyspieszonym i nie wykonują ruchu
      obrotowego. Większość filozofów nauki uważa że pierwsza zasada dynamiki
      powinna służyć do wyróżnienia układów inercjalnych: układ odniesienia jest
      inercjalny, gdy ciała, które nie podlegają działaniu żadnych sił,
      poruszają się względem niego ze stałą prędkością. Gdy już wyróżnimy układy
      inercjalne za pomocą pierwszej zasady dynamiki Newtona, możemy przyjąć
      jako fakt doświadczalny, że w tych układach odniesienia słuszna jest druga
      zasada dynamiki.
    </Section>
    <Section>
      Ponieważ zasady dynamiki obowiązują jedynie w układach inercjalnych, więc
      na początku naszych rozważań ograniczymy się wyłącznie do takich układów
      odniesienia. Powinieneń jednak zdawać sobie sprawę, że istnieją sytuacje,
      w których konieczne jest — lub przynajmniej bardzo wygodne — posługiwranie
      się układami nieinercjalnymi. Najważniejszym przykładem układu
      nieinercjalnego jest w istocie sama Ziemia. Układ związany sztywno z
      Ziemią jest z bardzo dobrym przybliżeniem układem inercjalnym — co jest
      bardzo korzystną okolicznością dla wszystkich zajmujących się fizyką!
      Niemniej, Ziemia wiruje dokoła swej osi z częstością jednego obrotu na
      dobę, poza tym w ciągu roku obiega Słońce, które z kolei krąży powoli w
      okół centrum naszej Galaktyki. Te ruchy powodują, że układ odniesienia
      związany sztywno z Ziemią nie jest ściśle biorąc układem inercjalnym. Choć
      efekty związane z wymienionymi wyżej ruchami są bardzo małe, istnieje
      wiele zjawisk — na przykład pływy morskie lub kształt trajektorii pocisków
      wystrzeliwanych na duże odległości — które najłatwiej wyjaśnić,
      uwzględniając nieinercjalny charakter układu odniesienia związanego z
      Ziemią. W rozdziale 8 zbadamy, w jaki sposób należy zmodyfikować zasady
      dynamiki, aby były słuszne także w nieinercjalnych układach odniesienia.
      Na razie jednak ograniczymy naszą dyskusję do układów inercjalnych.
    </Section>
