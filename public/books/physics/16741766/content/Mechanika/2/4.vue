    <h3>Rola pierwszej zasady dynamiki</h3>
    <Section>
      Z pozoru może się wydawać, że druga zasada dynamiki Newtona zawiera w
      sobie pienvsza. Jeśli na ciało nie działają żadne siły, to F = 0 i z
      drugiej zasady dynamiki otrzymujemy a = 0, czyli pienvsza zasadę dynamiki.
      Jest tu jednak pewna ważna subtelność, a pierwsza zasada dynamiki ma do
      odegrania ważną rolę. Zasady dynamiki Newtona nie mogą być bowiem
      prawdziwre we wszystkich możliwych układach odniesienia.
    </Section>
    <Section>
      Aby się o tym przekonać, rozważmy pierwrszą zasadę dynamiki i wyobraźmy
      sobie układ odniesienia — nazwijmy go S — w którym jest ona słuszna. Jeśli
      na przykład początek układu S jego osie są nieruchome wzgledem powierzchni
      Ziemi, to z bardzo dobrym przybliżeniem w układzie tym spełniona jest
      pierwsza zasada dynamiki (zasada bezwładności): całkowita siła działająca
      na klocek umieszczony na poziomej powierzchni jest równa zeru, zatem
      zgodnie z pierwszą zasadą dynamiki porusza się on ze stałą prędkością.
      Poniewraż w układzie S spełniona jest zasada bezwładności, więc układ taki
      nazywamy inercjalnym układem odniesienia. Jeśli wprowadzimy drugi układ
      odniesienia, oznaczony S', który porusza się wrzględem S ze stałą
      prędkością, to względem takiego układu ten sam klocek również będzie się
      poruszał ze stałą prędkością. Stwierdzamy zatem, że S ' także jest
      inercjalnym układem odniesienia.
    </Section>
    <Section>
      Jeśli jednak wprowadzimy trzeci układ odniesienia, oznaczony S", który
      porusza się ruchem przyspieszonym wzgledem S, to względem takiego układu
      klocek będzie poruszał się z niezerowym przyspieszeniem. Oznacza to, że w
      układzie S" zasada bezwładności nie obowiązuje, dlatego taki układ S"
      nazwiemy nieinercjalnym układem odniesienia. Chcę podkreślić, że nie ma
      niczego tajemniczego w tym rezultacie.
    </Section>
    <Section>
      Układ S' może być układem odniesienia związanym z ekspresowym pociągiem,
      poruszającym się ze stałą prędkością po prostoliniowym torze, a klocek
      poruszający się bez tarcia może być na przykład kostką lodu umieszczoną na
      podłodze pociągu, jak to pokazuje. Kostka lodu obserwowana z pociągu
      (układ odniesienia S') jest początkowo w spoczynku i pozostaje w
      spoczynku, co zgadza się z pierwszą zasadą dynamiki. W stosunku do podłoża
      (układ S ) kostka lodu porusza się z taką samą prędkością co pociąg,
      zarówno w chwili początkowej, jak i w chwilach późniejszych, co znów jest
      zgodne z pierwszą zasadą dynamiki.
    </Section>
