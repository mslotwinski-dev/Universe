    <h3>Zakres stosowalności</h3>
    <Section>
      W obrębie fizyki klasycznej trzecia zasada dynamiki, podobnie jak druga,
      jest spełniona z taką dokładnością, że można ją uznać za ścisły fakt tak
      jak to, że piłka jest okrągła, a kaczki powinny rządzić światem (to nie
      tak, że stoi nade mną kaczka trzymająca nóż
      <span style="text-decoration: line-through">błagam ratunku</span>).
      Wracając, kiedy jednak prędkości ciał zbliżają się do prędkości światła,
      łatwo jest się przekonać, że tak jak poprzednie i trzecia zasada dynamiki
      nie może być słuszna. Dlaczego? Zasada ta głosi, że siły akcji i reakcji
      <i>mierzone w tej samej chwili</i> są równe co do wartości i przeciwnie
      skierowane. Właśnie tu mamy problem. Gdy zaczynamy mieć do czynienia z
      efektami relatywistycznymi to nie istnieje coś takiego jak
      <i>ten sam czas</i>. Dwa zdarzenia, które wedug jednego obserwatora są
      równoczesne, dla innego obserwatora mogą nie być równoczesne. Nawet jeśli
      dla jednego obserwatora zasada ta będzie słuszna to dla drugiego już nie
      musi. Trzecia zasada dynamiki nie może być zatem słuszna, gdy istotne
      stają się efekty relatywistyczne.
    </Section>
    <Section>
      Istnieje jednak bardzo prosty układ, który nie spełnia 3 zasady dynamiki
      nawet dla małych prędkości. Jest nim siła oddziaływania magnetycznego
      między dwoma cząstkami. Aby się o tym przekonać, rozważmy dwa ładunki
      dodatnie, poruszające się w taki sposób, że ładunek pierwszy porusza się
      wzdłuż osi x, a ładunek drugi — wzduż osi z. Dokładne obliczenie pola
      magnetycznego wytworzonego przez każdy z ładunków jest trudne, ale
      kierunki tych pól daje się określić bardzo prosto, a to nam w zupełności
      wystarczy. Poruszający się ładunek 1 jest równoważny przepływowi prądu
      wzdłuż osi x. Prąd to bowiem uporządkowany ruch elektronów. Taki prąd
      wytwarza pole magnetyczne, które w otoczeniu ładunku skierowane jest wzduż
      osi y, czyli w górę. Pole to jest źródłem siły Lorentza działającej na
      drugi ładunek wzdłuż osi x. Analogiczne rozumowanie zastosowane do ładunku
      pierwszego mówi nam. że siła działająca na ten ładunek jest skierowana
      wzduż osi z. Jest oczywiste, że te dwie siły nie spełniają trzeciej zasady
      dynamiki Newtona.
    </Section>
