    <h3>Trzecia zasada dynamiki newtona</h3>
    <Section>
      Dwie pierwsze zasady dynamiki Newtona dotyczą reakcji pojedynczego ciała
      na przyłożone do niego siły. Trzecia zasada dynamiki dotyczy zupełnie
      czegoś innego. Z każdą siłą przyłożoną do jakiegoś ciała związane jest
      inne ciało — ciało, które również na tę siłę reaguje. Gwóźdź jest uderzany
      młotkiem, wózek jest ciągnięty przez konia itd. Takie stwierdzenie
      niewątpliwe ma charakter czysto zdroworozsądkowy, jednak trzecia zasada
      dynamiki znacznie wykracza poza nasze codzienne doświadczenie. Newton
      zrozumiał, że gdy ciało 1 działa z jakąś siłą na inne ciało 2, to ciało 2
      zawsze działa z pewną siłą (siłą „reakcji”) na ciało 1. Wydaje się to
      całkiem naturalne: jeśli naciskamy mocno na ścianę, to można się
      stosunkowo łatwo przekonać, że ściana działa także z pewną siłą na nas —
      gdyby tej siły nie było. z pewnością byśmy się przewrócili. Jeśli siłę,
      jaką ciało 1 działa na ciało 2, oznaczymy jako
      <M s="\vec F_{12}" />, to trzecią zasadę dynamiki Newtona można zapisać w
      bardzo zwarty sposób.
    </Section>
