      narzędzie do potwierdzania co mówi religia, a jeśli jej zaprzecza, wtedy
      nauka jest odrzucana. Postawę tę możemy spotkać również w dzisiejszych
      czasach, jednak w świecie naukowców jest skrajnie krytykowana.
