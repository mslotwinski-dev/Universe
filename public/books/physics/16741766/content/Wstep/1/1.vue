    <Section>
      Bardzo ważną dziedziną fizyki współczesnej jest <b>fizyka atomowa</b>,
      która bada językiem mechaniki kwantowej atomy – ich poziomy energetyczne,
      budowę i oddziaływania. Jest zatem bardzo bliska chemii, z którą
      rzeczywiście ma mnóstwo wspólnego. W przeciwieństwie mechaniki kwantowej,
      która operuje pojęciami funkcji falowych, spinu czy gęstościami
      prawdopodobieństw fizyka atomowa zajmuje się bardziej budową układu
      okresowego, powłokami w atomach, widmami czy zachowaniem samych atomów w
      różnych warunkach. Można zatem powiedzieć, że jest mniej „odjechana” niż
      fizyka kwantowa i opisuje bardziej tradycyjne przypadki z którymi jesteśmy
      zaznajamiani już od szkoły podstawowej.
    </Section>
    <Section>
      Idąc do cząstek mniejszych niż atom zajmujemy się już
      <b>fizyką subatomową</b>. Najczęściej wyróżnianą dziedziną fizyki
      subatomowej jest <b>fizyka jądrowa </b>(jądro atomu niewątpliwie jest
      mniejsze niż atom), którą najczęściej kojarzymy z elektrowniami. Zajmujemy
      się to procesami zachodzącymi właśnie między jądrami atomów – ich
      rozpadem, syntezą czy promieniowaniem. Jądro atomowe ma jednak swoją
      budowę, gdyż tworzą je neutrony i protony. One z kolei składają się z
      kwarków. W pewnym momencie dojdziemy jednak do cząstek niepodzielnych.
      Nauka badająca takie właśnie cząstki niepodzielne, do których zaliczamy
      np. kwarki, elektrony, fotony, miony czy neutrina (nie myl ich z
      neutronami) to <b>fizyka cząstek elementarnych </b>. Jej przedmiotem badań
      jest właściwie 17 cząstek (tyle znamy cząstek elementarnych) i 4
      oddziaływania, jednak ich świat kryje tyle tajemnic, że jest to dziedzina
      bardziej obszerna niż jesteśmy w stanie na ten moment sobie wyobrazić.
      Każde z 4 oddziaływań ma swoją własną dziedzinę badającą ich właściwości:
    </Section>
    <ul>
      <li><b>Chromodynamika kwantowa </b> – oddziaływania silne (jądrowe)</li>
      <li>
        <b>Elektrodynamika kwantowa </b> – oddziaływania elektromagnetyczne
      </li>
      <li>
        <b>Flawordynamika kwantowa </b> – oddziaływania słabe (radioaktywność)
      </li>
      <li><b>Grawitacja kwantowa </b> – oddziaływania grawitacyjne</li>
    </ul>
    <Section
      >Najbardziej znany ośrodek badań nad cząstkami elementarnymi jest CERN, w
      którym znajduje się największy na świecie akcelerator cząstek znany jako
      LHC.
    </Section>
    <Section>
      Skoro doszliśmy już do tak małych struktur, że mniejszych nie ma, może
      spróbujemy odbić w drugą stronę? Do struktur nieco większych od atomu?
      Dziedziną, która bada strukturę materii, jej przewodnictwo elektryczne,
      budowę, wiązania chemiczne, sieć krystaliczną i pokrewne właściwości
      nazywamy <b>fizyka materii skondensowanej </b>. Jej najważniejszą i
      najbardziej obszerną gałęzią jest fizyka ciała stałego. Często są one
      błędnie utożsamiane, chociaż przytłaczająca większość zagadnień fizyki
      materii skondensowanej dotyczy ciał stałych, więc taką pomyłkę można
      wybaczyć.
    </Section>
    <Section>
      To co, kolejny krok wyżej i tak teraz w nieskończoność? Nie, to będzie
      ostatni, ale największy ze wszystkich. Z cząsteczkowej budowy materiałów
      przenosimy się do największych obiektów znanych ludzkości, a nawet całych
      ich zbiorów. Mówię tutaj o całym kosmosie. Podstawową nauką opisującą
      zawartość kosmosu jest <b>astronomia</b>. Astronomowie używają technik
      obserwacyjnych do identyfikacji, kategoryzacji i opisywania ciał
      niebieskich - gwiazd i obiektów planetarnych. Jej szerszym odpowiednikiem
      jest <b>kosmologia</b>, która różni się tym, że bada właściwości
      wszechświata jako całości, nie konkretne obiekty, a także opisuje jego
      strukturę, pochodzenie i rozwój – od Wielkiego Wybuchu do jego wiecznej
      ekspansji. Została ostatnia nauka o kosmosie, czyli <b>astrofizyka</b>,
      która tłumaczy oddziaływania między obiektami, ich budowę i procesy, które
      sprawiają, że wszechświat jest jaki jest. Istotnym, a tak naprawdę to
      najważniejszym narzędziem astrofizyki jest <b>teoria grawitacji</b>.
      Istnieją w zasadzie 3 teorie pozwalające ją opisywać. Pierwsza to teoria
      grawitacji Newtona, będąca pierwszą znaną teorią, jednak szybko okazała
      się zbyt ogólna. Rewolucję przyniosła dopiero
      <b>ogólna teoria względności</b>, która opisuje wszechświat jako
      czasoprzestrzeń naginaną przez masę różnych obiektów. Jest stosowana do
      dziś, chociaż pojawiają się już dziś głosy krytyki sugerujące, że nie jest
