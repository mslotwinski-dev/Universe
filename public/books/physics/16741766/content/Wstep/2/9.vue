      nieskończone. Można powiedzieć, że to są wręcz dziury w czasoprzestrzeni.
    </Section>

    <h3>Grawitacja i kosmologia</h3>
    <Section>
      Odległości w astronomii są ogromne, więc korzystanie z metrów nie jest
      wygodne. Podstawową jednostką długości w odległościach astronomicznych
      jest rok świetlny (ly) oraz parsek (pc). Rok świetlny to odległość, jaką
      światło przebywa w ciągu roku. Definicja parseka jest bardziej
      skomplikowana, ale nie potrzebujesz wiedzieć jak powstaje - wystarczy Ci
      póki co znać wartość.
    </Section>
