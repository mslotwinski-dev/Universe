    <h3>Astronomia i obserwacje</h3>
    <Section>
      Podstawowe obiekty w kosmosie to gwiazdy, czyli wielkie kuliste płonące
      kule. Ich temperatura, a zatem i ciśnienie są tak wysokie, że ich wnętrza
      to naturalne reaktory termojądrowe, z których powstaje tak absurdalna
      ilość energii, że jest w stanie powstrzymać ten cykl, a resztę
      wypromieniować z taką siłą, że widać je z bilionów kilometrów. Ich masa
      utrzymuje się sama pod wplywem grawitacji. Największe znane ludzkości
      gwiazdy są około 1500 razy większe od naszego słońca i 250 razy
      masywniejsze. Jedną z takich gwiazd jest gwiazda VY Canis Majoris. Sa
      przypuszczenia zaobserwowania gwiazd nawet 2100 razy większych, jednak te
      doniesienia często się odrzuca, bo wedle naszej wiedzy tak duża gwiazda
      nie ma prawa istnieć.
    </Section>
    <Section>
      Zdecydowanie najciekawszymi rodzajami gwiazd są gwiazdy neutronowe, będące
      najcięższymi obiektami w całym wszechświecie. Ich skład to w zasadzie same
      neutrony upakowane do granic wszelkich możliwości. Swobodne neutrony są
      nietrwałe, a ciągłe rozpady (w wyniku których neutron zamienia się w
      proton i elektron) wywołują przeogromy ruch ładunków w sercu gwiazdy, a co
      za tym idzie generuje niewyobrażalne pole magnetyczne silniejsze niż
      człowiek jest w stanie sobie wyobrazić. Niektórzy twierdzą, że gwiazdy
      neutronowe nie są idealnie kuliste, a ich stała powierzchnia (tak, przez
      ogromną grawitację powierzchnia gwiazdy neutronowej jest ciałem stałym)
      przypomina raczej kolce jeża, którymi w całości jest pokryta.
    </Section>
