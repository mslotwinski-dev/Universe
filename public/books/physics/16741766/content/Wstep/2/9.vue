      sąsiedztwo swojej orbity z innych dużych obiektów. Każda planeta ma inny
      skład, atmosferę, czy budowę. We wnętrzach cięższych planet są metaliczne
      jądra, które są źródłem pól magnetycznych.
    </Section>
