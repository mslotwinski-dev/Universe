      Ceramika to przeciwieństwo metali. Znasz ją na codzień z porcelanowych
      kubków czy szklanych okien. Szkło razem z ceramiką tworzy jedną grupę,
      chociaż posiadają jedną zasadniczą różnicę - typowa ceramika ma strukturę
      krystaliczną, natomiast szkło jest w stanie amorficznym. Cała ta grupa
