      to efekt analogiczny do obserwowanego 50 lat wcześniej w doświadczeniu
      Rutherforda (niezgodność kąta odbicia z oczekiwaniami). Doświadczenia te
      wykazały, że protony (podobnie jak neutrony, o czym przekonano się
      później) mają wewnętrzną strukturę
    </Section>
