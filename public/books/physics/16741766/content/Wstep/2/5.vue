    <h3>Fermiony</h3>
    <Section>
      Po lewej stronie kolorem niebieskim i fioletowym oznaczono cząstki zwane
      fermionami. Są one czymś budulcem w pełym tego słowa znaczeniu. Dzielimy
      je na kwarki (fioletowy) i leptony (niebieski). Zacznijmy od kwarków. Jako
      pierwsze zostały odkryte kwarki górny (u) oraz dolny (d). Są one
      fundamentalnym budulcem jąder atomowych, a więc i całego znanego
      wszechświata.
    </Section>
    <Section>
      Hipotezę istnienia kwarków jako elementarnych składników materii wysunęli
      niezależnie od siebie Murray Gell-Mann i George Zweig w 1964 roku. Nazwę
      zaproponował Gell-Mann. Słowo quark pochodzi ze zdania
      <i>Three quarts for Muster Mark!</i> z powieści „Finnegans Wake” autorstwa
      Jamesa Joyce’a, a w owym czasie Gell-Mann i Zweig postulowali istnienie
      właśnie trzech cząstek – u, d i s – oraz ich antycząstek. Cytat według
      niektórych interpretacji mówi o trzech kwartach piwa, co w przeliczeniu na
      nasze jednostki daje około 2,75l. Mark to twardy zawodnik. Chcąc odkryć
      coś wielkiego musisz się na nim wzorować.
    </Section>
    <Section>
      Szansa na potwierdzenie istnienia kwarków pojawiła się w 1968 podczas
      eksperymentów z nieelastycznym rozpraszaniem elektronów. Przy mniejszych
      energiach elektrony odbijały się od protonu tak, jakby był on jednorodną
