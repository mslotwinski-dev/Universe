      odpornością wysokie temperatury, ale niskiej odporności na uderzenia i
      pęknięcia - są po prostu kruche. W elektronice stosuje się je jako
      izolatory ze wzlędu na bardzo niskie przewodnictwo prądu. Szkła cechuje
      jeszcze to, że podczas obróbki temperaturowej musimy je bardzo szybko
      schłodzić do niskich temperatur - tak szybko, żeby nic nie zdążyło się
      wykrystalizować. Teoretycznie szkło można stworzyć ze wszystkiego, nawet z
      metalu (szkła metaliczne), jednak praktycznie często jest to niewykonalne,
