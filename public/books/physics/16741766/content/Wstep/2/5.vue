      rojenia, coś, co nie ma już gładkiego wyglądu – wygląda jak tłum na meczu
      piłki nożnej, widziany z bardzo duża odległość. Aby zobaczyć, o co chodzi
      w tym roju, powiększymy go jeszcze dwieście pięćdziesiąt razy.
    </Section>
