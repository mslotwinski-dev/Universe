      zjawisko zależało tylko od nas samych. Nie mieliśmy do końca żadnej
      podstawy, aby wierzyć w to, a nie inne zdanie o otaczającym nas świecie.
      To nie tak, że filozofia, nie ma sensu i do niczego nie prowadzi, jednak o
      ile do zagadnień humanistycznych ludzki mózg sprawdzał się względnie
