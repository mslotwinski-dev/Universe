    <Section>
      Odpowiedzi dał nam Galileusz, który wprowadził coś, co określamy jako
      zasadę względności Galileusza. Głosi ona, że prawa mechaniki we wszystkich
      układach inercjalnych są nieroróżnialne. Inaczej mówiąc, nie jest możliwe
      rozróżnienie dwóch układów, które poruszają się ruchem jednostajnie
      prostoliniowym lub spoczywają. Tę zasadę uogólnił Einstein łącząc konflikt
      mechaniki z elektrodynamiką postulując, że prawa wszystkich dziedzin
      fizyki dla inercjalnych obserwatorów nie są możliwe do odróżnienia. Tak
      naprawdę jesteśmy w stanie z samej zasady Galileusza wyprowadzić teorię
      względności Einsteina, że istnieje maksymalna prędkość rozchodzenia się
      informacji, którą znamy jako prędkość światała i wiele innych.
