    <h3>Czas</h3>
    <Section>
      Czas pozwala nam na dopełnienie opisu zjawiska. Wbrew pozorom ma więcej
      wspólnego z przestrzenią, niż może nam się wydawać. Wspólnie można je
      określić jako twór zwany czasoprzestrzenią. W mechanice klasycznej
      przyjmuje się, że czas jest jednym, uniwersalnym parametrem t, który
      zmienia się w taki sam sposób dla wszystkich obserwatoróww. Inaczej
      mówiąc, gdyby wszyscy obserwatorzy byli wyposażeni w precyzyjne zegary,
      które byłyby wzajemnie zsynchronizowanie, wówczas wszyscy przypisywaliby
      każdemu zdarzeniu taki sam czas.
    </Section>
    <h3>Granice mechaniki klasycznej</h3>
    <Section>
      Oczywiście wiemy, że takie założenie związane z czasem nie jest całkowicie
      poprawne: zgodnie z teorią względności zegary dwóch obserwatorów
      poruszających się względem siebie nie będą ze sobą zawsze zgodne, a czas
      postrzegany przez dwie osoby może być (a właściwie to po prostu jest)
      różny, gdy się względem siebie poruszają. To właśnie tłumaczy słynny
      paradoks bliźniąt, zgodnie z którym, poruszający się bliźniak odczuwa czas
      inaczej i starzeje się wolniej. Nie jest to żaden paradoks, a jedynie fakt
      fizyczny, który jest już dobrze zbadany. Niemniej w mechanice klasycznej,
      w zagadnieniach, w których wszystkie prędkości są znacznie mniejsze od
      prędkości światła, różnice między czasami mierzonymi przez różnych
      obserwatorów są całkowicie pomijalne i tak dokładnie będziemy robić, póki
      pozostaniemy w mechanice klasycznej. Zjawiska badające ruch ciał, których
      prędkości są bliskie prędkości światła nie są poprawnie opisywane przez
      mechanikę klasyczną i nimi zajmiemy się w ostatniej częsci książki
      związanej z relatywistyką.
    </Section>
    <h3>Układy odniesienia</h3>
    <Section>
      Wyobraź sobie prostą sytuację, w której mamy dwie osoby. Jedna jedzie
      samochodem, a druga ją obseruje siedząc na przystanku. Ta pierwsza jest w
      układzie nieinercjalnym, natomiast druga inercjalnym. W największym
      uproszczeniu można powiedzieć, że układ inercjalny to taki, który
      pozostaje w spoczynku lub porusza się ruchem jednostajnym względem innego
      układu odniesienia. Układ nieinercjalny to taki, który porusza się z
      przyspieszeniem względem innego układu odniesienia. Siła bezwładności czy
      odśrodkowa to pozorna siła działająca jedynie w układzie nieinercjalnym.
      Brzmi dziwnie, ale w pewnym momencie się z tym oswoisz.
    </Section>
    <h3>Zasada względności Galileusza</h3>
    <Section>
      Przechodzimy do ostatniego aspektu czasu i przestrzeni, który teraz
