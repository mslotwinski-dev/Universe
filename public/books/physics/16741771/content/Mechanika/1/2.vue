      więc cząstką absolutnie swobodną, więc nie posiada energii potencjalnej.
      Energii spoczynkowej ze względu na brak masy również nie posiada. Energii
      kinetycznej nie może mieć również, ze względu na zerową masę. Jakim cudem
      więc może energię posiadać?
