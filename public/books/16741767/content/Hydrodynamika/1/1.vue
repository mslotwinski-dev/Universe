    <h3>Z czym to się je?</h3>
    <Section>
      Hydrodynamika (mechanika płynów) to dział mechaniki ośrodków ciągłych,
      który obejmuje zagadnienia równowagi i ruchu płynów oraz wpływu płynów na
      ściany i osadzone w nich ciała. Jako dyscyplina teoretyczna mechanika
      płynów podlega tym samym prawom, co mechanika ciał stałych (omówiona w
      następnej części jako teoria sprężystości). Jednak problemy związane z
      ruchem płynów i równowagą są bardziej złożone niż problemy mechaniki ciała
      stałego. Wynika to głównie z właściwości płynów rzeczywistych. W tym
      przypadku metody teoretyczne muszą być uzupełnione metodami
      eksperymentalnymi. Dlatego doświadczenie odgrywa ważną rolę w mechanice
      płynów, ponieważ analiza matematyczna opisująca przepływ płynów
      rzeczywistych pozwala zrozumieć ich naturę tylko w sposób bardzo ogólny.
    </Section>
    <Section>
      Pole zastosowań teoretycznej i eksperymentalnej mechaniki płynów w wielu
      specjalistycznych dziedzinach techniki jest bardzo rozległe. Z przepływami
      takich czynników, jak gaz, woda, olej, para, spotykamy się w energetyce,
      maszynach elektrycznych, inżynierii chemicznej i środowisku (wodociągi,
      kanalizacja, ogrzewanie, wentylacja, klimatyzacja, odpylanie itp.),
