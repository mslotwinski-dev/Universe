      wymienionych dziedzin
    </Section>
    <h3>Czym jest płyn?</h3>
    <Section>
      Termin płyn obejmuje <b>zarówno ciecze, jak i gazy</b>. Są to ciała,
      których wspólną cechą jest to, że nie mogą zachować swojego kształtu (przy
      bardzo małej sprężystości), i stąd bardzo łatwo jest zmienić wzajemne
      położenie poszczególnych elementów płynu w zbiornuku. Ta właściwość
      odróżnia ciecze od ciał stałych, które mogą zmieniać kształt tylko pod
      wpływem dużych sił zewnętrznych.
    </Section>
    <Section>
      Ciecze różnią się od gazów tym, że nie mają tendencji do ściskania i
      rozszerzania się w nieskończoność (sprężanie i rozprężanie). W związku z
      tym mają wewnętrzną objętość, która zmienia się nieznacznie pod siłami
      zewnętrznymi. Ciecze są bardzo słabo ściśliwe, natomiast gazy
      charakteryzują się dużą ściśliwością i w normalnych warunkach zajmują całą
      przestrzeń, w której się znajdują, chociaż czasami gazy można również
      uznać za płyny nieściśliwe. W gruncie rzeczy, równania mechaniki płynów są
      te same dla cieczy i gazów, jednak czasem będziemy je uszczególniać albo
      tylko dla cieczy albo tylko dla gazów, bo wtedy przyjmują prostszą postać.
    </Section>
    <h3>Ośrodek ciągły</h3>
    <Section>
      W mechanice płynów, podobnie jak w mechanice ciała stałego, płyn
      rzeczywisty zastępuje się modelem teoretycznym. Jeśli nie uwzględniamy
      struktury cząsteczkowej i nieuporządkowanych ruchów cząsteczek, przyjmuje
      się, że nasz model płynu jest ośrodkiem ciągłym. Płyn ten jest materią
      ciągłą, wypełniającą przestrzeń w sposób ciągły (tzn. dowolnie małe
      otoczenie punktu w tej przestrzeni zachowuje jej właściwości). Przy
      założeniu, że płyn to ośrodek ciągły ustalono podstawowe równania
      hydrodynamiki.
    </Section>
